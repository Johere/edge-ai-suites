def get_known_field_codes() -> set:
    """Flat set of every placeholder code the pipeline can fill."""
    return {f["code"] for g in REPORT_TEMPLATE_FIELD_GROUPS for f in g["fields"]}


def get_field_kinds() -> dict:
    """Map every known field code to its ``kind`` ("raw" | "generated").

    Drives the split-fill pipeline: "raw" codes are filled directly from measured
    data, "generated" codes go to the LLM. Codes absent from this map (custom
    template placeholders) are treated as generated.
    """
    return {f["code"]: f["kind"] for g in REPORT_TEMPLATE_FIELD_GROUPS for f in g["fields"]}


def get_always_on_codes() -> set:
    """Codes always included (auto metadata, e.g. report_time) — not user-toggleable."""
    return {f["code"] for g in REPORT_TEMPLATE_FIELD_GROUPS for f in g["fields"]
            if f.get("always_on")}


def get_manual_field_codes() -> set:
    """Codes the teacher types in manually (not derivable from audio/video)."""
    return {f["code"] for g in REPORT_TEMPLATE_FIELD_GROUPS for f in g["fields"]
            if f.get("input") == "manual"}


def _validate_catalog() -> None:
    """Fail fast at import time on a malformed catalog (typo'd kind, dup code)."""
    seen = set()
    for group in REPORT_TEMPLATE_FIELD_GROUPS:
        for field in group["fields"]:
            code = field["code"]
            if code in seen:
                raise ValueError(f"Duplicate field code in catalog: {code!r}")
            seen.add(code)
            if field["kind"] not in FIELD_KINDS:
                raise ValueError(f"Field {code!r} has invalid kind {field['kind']!r}")


_validate_catalog()
