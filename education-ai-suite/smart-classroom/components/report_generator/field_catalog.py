"""
Report field catalog — the domain data contract for classroom reports.

Declares which placeholders a classroom report can contain, grouped and
bilingually labeled for the online editor's "Available Data" reference panel.
This is business knowledge (what a report is made of), deliberately kept apart
from the generic docx mechanics in ``template_manager``.

Each field carries a ``kind`` recording *how it is filled*:
  - "raw":       a value/metric that comes directly from data or metadata and
                 must never be invented by the LLM (numbers, names, timestamps).
  - "generated": free-text that genuinely requires LLM summarization/assessment.

``kind`` drives the split-fill pipeline (see ``report_generator`` +
``raw_field_mapper``): "raw" fields are filled directly from measured data,
only "generated" fields are sent to the LLM. Several "raw" fields have no
structured data source wired up yet and fall back to a neutral no-data marker.
"""

